@Autonomous(name="Auto Blue", group="Autonomous")

public class AutoBlue extends LinearOpMode {
    private Maccabot robot;
    private double correction;

    // Declare OpMode members.
    private ElapsedTime runtime = new ElapsedTime();

    public void runOpMode() throws InterruptedException {
        robot = new Maccabot(this);
        robot.initializeRobot();


        while (!isStopRequested() && !robot.isGyroCalibrated())
        {
            sleep(50);
            idle();
        }

        // Wait for the game to start (driver presses PLAY)
        waitForStart();
        runtime.reset();

        while (opModeIsActive()) {

            robot.auto_forward(850, 0.65);
            while (opModeIsActive() && robot.encoderIsBusy()) {
                idle();
            }
            sleep(1000);
            robot.setpower0();
            robot.runwithoutencoder();
            robot.rotate(-90, 0.65);
        }
        stop();

    }

}
